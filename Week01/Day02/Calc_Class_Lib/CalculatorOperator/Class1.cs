namespace CalculatorOperator;

public class Operation{
    public static int Add(int n1, int n2){
        return n1 + n2;
    }

    public static int Sub(int n1, int n2){
        return n1 - n2;
    }

    public static int Mul(int n1, int n2){
        return n1 * n2;
    }
    public static int Div(int n1, int n2){
        return n1 / n2;
    }
}
